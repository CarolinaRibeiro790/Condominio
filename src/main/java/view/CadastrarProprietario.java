    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // 1. Criar objeto Proprietario com os dados do formulário
        Proprietario proprietario = new Proprietario();

        // 2. Preencher os dados 
        proprietario.setCodigoEndereco(Integer.parseInt(txtCodigoResidencia.getText()));
        proprietario.setNome(txtNome.getText());

        // Formatar a data de dd/MM/yyyy para yyyy-MM-dd
        String dataDigitada = txtNascimento.getText();
        try {
            SimpleDateFormat formatoEntrada = new SimpleDateFormat("dd/MM/yyyy");
            SimpleDateFormat formatoBanco = new SimpleDateFormat("yyyy-MM-dd");
            Date data = formatoEntrada.parse(dataDigitada); // Converte String → Date
            String dataFormatada = formatoBanco.format(data); // Converte Date → String no formato do banco
            proprietario.setDataNascimento(dataFormatada);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Formato de data inválido! Use dd/MM/yyyy.");
            return;
        }
        
        proprietario.setCpf(txtCpf.getText().trim());
        proprietario.setRg(txtRg.getText().trim());
        
        try {
            //Chamar o Controller para salvar
            boolean sucesso = controller.cadastrarProprietario(proprietario);
            
            if (sucesso) {
                JOptionPane.showMessageDialog(null, proprietario != null ? proprietario.toString() : "Proprietario não cadastrado.");
                voltarMenu();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao salvar!");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(CadastrarProprietario.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

