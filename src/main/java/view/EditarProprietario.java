    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        try {
            int codigoProprietario = Integer.parseInt(txtCodigo.getText());
            Proprietario proprietarioAtualizado = new Proprietario();

            proprietarioAtualizado.setCodigo(codigoProprietario);
            proprietarioAtualizado.setNome(txtNome.getText());
            proprietarioAtualizado.setDataNascimento(txtNascimento.getText());
            proprietarioAtualizado.setCpf(txtCpf.getText());
            proprietarioAtualizado.setRg(txtRg.getText());
            proprietarioAtualizado.setCodigoEndereco(Integer.parseInt(txtResidencia.getText()));

            boolean sucesso = controller.editarProprietario(proprietarioAtualizado);

            if (sucesso) {
                JOptionPane.showMessageDialog(null, proprietarioAtualizado != null ? proprietarioAtualizado.toString() : "Morador não editado.");
                limparCampos();
                voltarMenu();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao salvar!");
            }

        } catch (SQLException | ParseException ex) {
            Logger.getLogger(EditarMorador.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int codigoProprietario = Integer.parseInt(txtCodigo.getText());

        ProprietarioController controller = null;
        try {
            controller = new ProprietarioController();
        } catch (SQLException ex) {
            Logger.getLogger(EditarMorador.class.getName()).log(Level.SEVERE, null, ex);
        }
        boolean sucesso;
        try {
            sucesso = controller.deletarProprietario(codigoProprietario);
            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Proprietário deletado com sucesso!");
                limparCampos();
                //atualizarTabela(); 
            } else {
                JOptionPane.showMessageDialog(null, "Proprietário não encontrado");
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditarProprietario.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnExcluirActionPerformed
    private void limparCampos() {
        txtCod.setText("");
        txtNome.setText("");
        txtNascimento.setText("");
        txtRg.setText("");
        txtCpf.setText("");
    }

