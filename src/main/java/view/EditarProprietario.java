    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        try {
            int codigoPesquisa = Integer.parseInt(txtCod.getText().trim());

            // Primeiro busca o morador existente
            Proprietario proprietarioExiste = controller.pesquisarProprietario(codigoPesquisa);

            //Formatar a data 
            SimpleDateFormat formatoBanco = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat formatoBrasil = new SimpleDateFormat("dd/MM/yyyy");

            Date data = formatoBanco.parse(proprietarioExiste.getDataNascimento());  // Converte String → Date
            String dataFormatada = formatoBrasil.format(data);  // Converte Date → String formatada

            txtNascimento.setText(dataFormatada);

            if (proprietarioExiste != null) {
                // Preenche os campos do formulário com os dados existentes
                txtCodigo.setText(String.valueOf(proprietarioExiste.getCodigo()));
                txtNome.setText(proprietarioExiste.getNome());
                txtNascimento.setText(dataFormatada);
                txtRg.setText(proprietarioExiste.getRg());
                txtCpf.setText(proprietarioExiste.getCpf());
                txtResidencia.setText(String.valueOf(proprietarioExiste.getCodigoEndereco()));
            } else {
                JOptionPane.showMessageDialog(null, "Proprietário não encontrado!");
                limparCampos();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Proprietário não encontrado!");
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

