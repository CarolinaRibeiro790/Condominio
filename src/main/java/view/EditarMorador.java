    private void txtPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtPesquisarActionPerformed
        try {
            int codigoPesquisa = Integer.parseInt(txtCodigo.getText().trim());

            // Primeiro busca o morador existente
            Morador moradorExiste = controller.pesquisarMorador(codigoPesquisa);

            //Formatar a data 
            SimpleDateFormat formatoBanco = new SimpleDateFormat("yyyy-MM-dd");
            SimpleDateFormat formatoBrasil = new SimpleDateFormat("dd/MM/yyyy");

            Date data = formatoBanco.parse(moradorExiste.getDataNascimento());  // Converte String → Date
            String dataFormatada = formatoBrasil.format(data);  // Converte Date → String formatada

            txtNasc.setText(dataFormatada);

            if (moradorExiste != null) {
                // Preenche os campos do formulário com os dados existentes
                txtCodigo.setText(String.valueOf(moradorExiste.getCodigo()));
                txtNome.setText(moradorExiste.getNome());
                txtNasc.setText(dataFormatada);
                txtRg.setText(moradorExiste.getRg());
                txtCpf.setText(moradorExiste.getCpf());
                txtResidencia.setText(String.valueOf(moradorExiste.getCodigoEndereco()));

            } else {
                JOptionPane.showMessageDialog(null, "Morador não encontrado!");
                limparCampos();
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Morador não encontrado!");
        }
    }//GEN-LAST:event_txtPesquisarActionPerformed

    private void txtEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtEditarActionPerformed
        try {
            int codigoMorador = Integer.parseInt(txtCodigo.getText());
            Morador moradorAtualizado = new Morador();

            moradorAtualizado.setNome(txtNome.getText());
            moradorAtualizado.setDataNascimento(txtNasc.getText());
            moradorAtualizado.setCpf(txtCpf.getText());
            moradorAtualizado.setRg(txtRg.getText());
            moradorAtualizado.setCodigoEndereco(Integer.parseInt(txtResidencia.getText()));
            moradorAtualizado.setCodigo(codigoMorador);

            boolean sucesso = controller.editarMorador(moradorAtualizado);

            if (sucesso) {
                JOptionPane.showMessageDialog(null, moradorAtualizado != null ? moradorAtualizado.toString() : "Morador não editado.");
                limparCampos();
                voltarMenu();
            } else {
                JOptionPane.showMessageDialog(null, "Erro ao salvar!");
            }

        } catch (SQLException ex) {
            Logger.getLogger(EditarMorador.class.getName()).log(Level.SEVERE, null, ex);
        } catch (ParseException ex) {
            Logger.getLogger(EditarMorador.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_txtEditarActionPerformed

