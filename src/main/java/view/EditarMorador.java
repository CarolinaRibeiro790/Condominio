    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        int codigoMorador = Integer.parseInt(txtCodigo.getText());

        MoradorController controller = null;
        try {
            controller = new MoradorController();
        } catch (SQLException ex) {
            Logger.getLogger(EditarMorador.class.getName()).log(Level.SEVERE, null, ex);
        }
        boolean sucesso;
        try {
            sucesso = controller.deletarMorador(codigoMorador);
            if (sucesso) {
                JOptionPane.showMessageDialog(null, "Morador deletado com sucesso!");
                limparCampos();

            } else {
                JOptionPane.showMessageDialog(null, "Morador não encontrado");
            }
        } catch (SQLException ex) {
            Logger.getLogger(EditarMorador.class.getName()).log(Level.SEVERE, null, ex);
        }


    }//GEN-LAST:event_btnExcluirActionPerformed
